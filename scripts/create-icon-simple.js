// 创建最简单的 ICO 文件
function createMinimalICO() {
    console.log('创建最小 ICO 文件...\n');
    
    // ICO 文件格式：
    // 文件头 (6 bytes) + 目录项 (16 bytes * n) + 图像数据
    
    // 16x16 空白图标数据 (PNG 格式)
    // 这是一个透明的 16x16 PNG
    const png16 = Buffer.from([
        0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D,
        0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x10, 0x00, 0x00, 0x00, 0x10,
        0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0xF3, 0xFF, 0x61, 0x00, 0x00, 0x00,
        0x0D, 0x49, 0x44, 0x41, 0x54, 0x78, 0x9C, 0x63, 0xFC, 0xCF, 0xC0, 0x00,
        0x00, 0x00, 0x03, 0x00, 0x01, 0x00, 0x05, 0xFE, 0xD4, 0x16, 0x2C, 0x00,
        0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
    ]);
    
    // 32x32 空白图标数据
    const png32 = Buffer.from([
        0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D,
        0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x20, 0x00, 0x00, 0x00, 0x20,
        0x08, 0x06, 0x00, 0x00, 0x00, 0x73, 0x7A, 0x7A, 0xF4, 0x00, 0x00, 0x00,
        0x0D, 0x49, 0x44, 0x41, 0x54, 0x78, 0x9C, 0x63, 0xFC, 0xCF, 0xC0, 0x00,
        0x00, 0x00, 0x03, 0x00, 0x01, 0x00, 0x05, 0x7E, 0x2C, 0x44, 0xB8, 0x00,
        0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
    ]);
    
    // 48x48 空白图标
    const png48 = Buffer.from([
        0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D,
        0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x30, 0x00, 0x00, 0x00, 0x30,
        0x08, 0x06, 0x00, 0x00, 0x00, 0x57, 0x02, 0xF9, 0x87, 0x00, 0x00, 0x00,
        0x0D, 0x49, 0x44, 0x41, 0x54, 0x78, 0x9C, 0x63, 0xFC, 0xCF, 0xC0, 0x00,
        0x00, 0x00, 0x03, 0x00, 0x01, 0x00, 0x05, 0xDD, 0x0A, 0xD9, 0xA4, 0x00,
        0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
    ]);
    
    // 256x256 空白图标（稍微大一点）
    const png256 = Buffer.from([
        0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D,
        0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, 0x00,
        0x08, 0x06, 0x00, 0x00, 0x00, 0x5C, 0x72, 0xA8, 0x66, 0x00, 0x00, 0x00,
        0x0D, 0x49, 0x44, 0x41, 0x54, 0x78, 0x9C, 0x63, 0xFC, 0xCF, 0xC0, 0x00,
        0x00, 0x00, 0x03, 0x00, 0x01, 0x00, 0x05, 0xDD, 0x0A, 0xD9, 0xA4, 0x00,
        0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
    ]);
    
    const images = [
        { size: 16, data: png16 },
        { size: 32, data: png32 },
        { size: 48, data: png48 },
        { size: 0, data: png256 }  // 0 表示 256
    ];
    
    // 计算偏移量
    let offset = 6 + (images.length * 16);
    const entries = images.map(img => {
        const entry = {
            width: img.size,
            height: img.size,
            colorCount: 0,
            reserved: 0,
            planes: 1,
            bitCount: 32,
            sizeInBytes: img.data.length,
            offset: offset
        };
        offset += img.data.length;
        return entry;
    });
    
    // 写入文件头
    const header = Buffer.alloc(6);
    header.writeUInt16LE(0, 0); // 保留
    header.writeUInt16LE(1, 2); // 类型: ICO
    header.writeUInt16LE(images.length, 4); // 图像数量
    
    // 写入目录
    const directory = Buffer.alloc(images.length * 16);
    entries.forEach((entry, index) => {
        const pos = index * 16;
        directory.writeUInt8(entry.width, pos);
        directory.writeUInt8(entry.height, pos + 1);
        directory.writeUInt8(entry.colorCount, pos + 2);
        directory.writeUInt8(entry.reserved, pos + 3);
        directory.writeUInt16LE(entry.planes, pos + 4);
        directory.writeUInt16LE(entry.bitCount, pos + 6);
        directory.writeUInt32LE(entry.sizeInBytes, pos + 8);
        directory.writeUInt32LE(entry.offset, pos + 12);
    });
    
    // 合并
    const parts = [header, directory, ...images.map(img => img.data)];
    const icoPath = path.join(assetsDir, 'icon.ico');
    fs.writeFileSync(icoPath, Buffer.concat(parts));
    
    console.log('✅ 已创建最小 ICO 文件:', icoPath);
    console.log('   包含尺寸: 16x16, 32x32, 48x48, 256x256');
    console.log('\n⚠️  注意: 这是一个空白透明图标');
    console.log('   如需自定义图标，请使用以下方法之一:');
    console.log('   1. 运行: node scripts/create-icon.js (推荐，需要安装 sharp)');
    console.log('   2. 访问 https://convertio.co/zh/svg-ico/ 在线转换');
    console.log('   3. 使用 Photoshop/GIMP 等工具手动创建');
}

